/**
 * op_add -  add a to b.
 * @a: int number
 * @b: int number
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub -  subtraction between a and b.
 * @a: int number
 * @b: int number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -  multiplication between a and b.
 * @a: int number
 * @b: int number
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div -  division between a and b.
 * @a: int number
 * @b: int number
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod -  module between a and b.
 * @a: int number
 * @b: int number
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
